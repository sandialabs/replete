package replete.text;

// This class attempts to keep track of strings
// that are commonly used in constructing
// user interfaces.

public class StringLib {
    // Can toUpperCase() or toLowerCase(), but this is the standard for user-facing strings
    public static final String NONE    = "(None)";   // Parentheses here generally help separate from a real value
    public static final String N_A     = "(N/A)";
    public static final String NULL    = "(NULL)";   // All-caps indicate a "programming" term over a regular term
    public static final String UNKNOWN = "(Unknown)";
    public static final String PENDING = "(Pending)";
}
