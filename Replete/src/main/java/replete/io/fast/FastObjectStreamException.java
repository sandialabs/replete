package replete.io.fast;


public class FastObjectStreamException extends RuntimeException {
    public FastObjectStreamException() {
        super();
    }
    public FastObjectStreamException(String message, Throwable cause) {
        super(message, cause);
    }
    public FastObjectStreamException(String message) {
        super(message);
    }
    public FastObjectStreamException(Throwable cause) {
        super(cause);
    }
}
