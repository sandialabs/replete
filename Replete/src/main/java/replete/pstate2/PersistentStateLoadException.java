package replete.pstate2;

public class PersistentStateLoadException extends RuntimeException {
    public PersistentStateLoadException() {
        super();
    }
    public PersistentStateLoadException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
    public PersistentStateLoadException(String arg0) {
        super(arg0);
    }
    public PersistentStateLoadException(Throwable arg0) {
        super(arg0);
    }
}
