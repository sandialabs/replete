package replete.params.hier;

public class PropertySetRootException extends PropertySetResolutionException {

    public PropertySetRootException() {
        super();
    }

    public PropertySetRootException(String message, Throwable cause, boolean enableSuppression,
                                        boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public PropertySetRootException(String message, Throwable cause) {
        super(message, cause);
    }

    public PropertySetRootException(String message) {
        super(message);
    }

    public PropertySetRootException(Throwable cause) {
        super(cause);
    }

}
