package replete.params.hier;

public class PropertySetResolutionException extends RuntimeException {

    public PropertySetResolutionException() {
        super();
    }

    public PropertySetResolutionException(String message, Throwable cause, boolean enableSuppression,
                                        boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public PropertySetResolutionException(String message, Throwable cause) {
        super(message, cause);
    }

    public PropertySetResolutionException(String message) {
        super(message);
    }

    public PropertySetResolutionException(Throwable cause) {
        super(cause);
    }

}
