package finio.core.errors;


public class KeyPathHierarchyException extends KeyPathException {


    //////////////////
    // CONSTRUCTORS //
    //////////////////

    public KeyPathHierarchyException() {
        super();
    }
    public KeyPathHierarchyException(String message, Throwable cause, Type type) {
        super(message, cause, type);
    }
    public KeyPathHierarchyException(String message, Throwable cause) {
        super(message, cause);
    }
    public KeyPathHierarchyException(String message, Type type) {
        super(message, type);
    }
    public KeyPathHierarchyException(String message) {
        super(message);
    }
    public KeyPathHierarchyException(Throwable cause, Type type) {
        super(cause, type);
    }
    public KeyPathHierarchyException(Throwable cause) {
        super(cause);
    }
    public KeyPathHierarchyException(Type type) {
        super(type);
    }
}
